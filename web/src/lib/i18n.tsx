const dictionaries: Record<Language, Dictionary> = {
  ka: {
    ...Object.fromEntries(Object.entries(rawDictionaries.ka).map(([key, value]) => [key, repairMojibake(value)])),
    ...dictionaryOverlays.ka,
  },
  en: {
    ...rawDictionaries.en,
    ...dictionaryOverlays.en,
  },
};

type I18nContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return saved === "ka" || saved === "en" ? saved : getBrowserLanguage();
  });

  useEffect(() => {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language;
  }, [language]);

  useEffect(() => {
    let cancelled = false;

    async function syncLanguageFromBackend() {
      if (!auth.getToken()) {
        return;
      }

      try {
        const response = await api.get("/api/me");
        const nextLanguage = normalizeLanguage(response.data?.language);

        if (!cancelled) {
          setLanguageState(nextLanguage);
        }
      } catch {
        // Keep local preference when backend lookup fails.
      }
    }

    void syncLanguageFromBackend();

    return () => {
      cancelled = true;
    };
  }, []);

  function setLanguage(language: Language) {
    setLanguageState(language);

    if (!auth.getToken()) {
      return;
    }

    void api.post("/api/me/language", { language }).catch(() => {
      // Keep the UI responsive even if preference sync fails.
    });
  }

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key, vars) => {
        const template = dictionaries[language][key] ?? dictionaries.en[key] ?? key;
        if (!vars) return template;

        return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? `{${name}}`));
      },
    }),
    [language],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}
